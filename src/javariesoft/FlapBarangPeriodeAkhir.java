    private void btnPreviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviewActionPerformed
        // TODO add your handling code here:
        HashMap parameter = new HashMap();
        JasperPrint jasperPrint = null;
        String periode=(txtTahun.getText() + "." + (cboBulan.getSelectedIndex() + 1));
        String perblnthn=((cboBulan.getSelectedItem())+ "-" +txtTahun.getText() );
        parameter.put("PPeriode",periode);
        parameter.put("PBlnTahun",perblnthn);
        if(RadioMerk.isSelected()){
            if (pilihan.isSelected()) {
                parameter.put("Pmerk", "%");
            } else {
                parameter.put("Pmerk", txtkode.getText());
            }
        }else if(RadioKategori.isSelected()){
            if (pilihan.isSelected()) {
                parameter.put("Pkategori", "%");
            } else {
                parameter.put("Pkategori", txtkode.getText());
            }
        }
        try {
            URL url = null;
            if(RadioMerk.isSelected()){
                url = new URL(global.REPORT+ "/BarangPeriodeMerk.jasper");
            }else if(RadioKategori.isSelected()){
                url = new URL(global.REPORT+ "/BarangPeriode.jasper");
            }
            InputStream in = url.openStream();
            jasperPrint = JasperFillManager.fillReport(in, parameter, c);

            JasperViewer.viewReport(jasperPrint, false);
        } catch (Exception ex) {
            System.out.print(ex.toString());
            //Logger.getLogger(formlaporan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPreviewActionPerformed

