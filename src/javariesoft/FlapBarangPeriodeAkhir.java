    void reloadData() {
        try {
            JDBCAdapter j = new JDBCAdapter(c);
            String sql="";
            if(RadioMerk.isSelected()){
                sql="Select * from JENISBARANG where ID like '" + txtNama.getText() + "%' or lower(JENIS) like '%" + txtNama.getText().toLowerCase() + "%'";
            }else if(RadioKategori.isSelected()){
                sql="Select * from KATEGORI where IDKATEGORI like '" + txtNama.getText() + "%' or lower(KATEGORI) like '%" + txtNama.getText().toLowerCase() + "%'";;
            }
            j.executeQuery(sql);
            jScrollPane1.getViewport().remove(jTable1);
            jTable1 = new JTable(j);
            jTable1.addKeyListener(new java.awt.event.KeyAdapter() {

                public void keyPressed(java.awt.event.KeyEvent evt) {
                    jTable1KeyPressed(evt);
                }
            });
            jScrollPane1.getViewport().add(jTable1);
            j.close();
        } catch (Exception ex) {
            Logger.getLogger(FlapBarangPeriodeAkhir.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
