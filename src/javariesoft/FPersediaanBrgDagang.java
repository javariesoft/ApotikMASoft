    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        HashMap parameter = new HashMap();
    JasperPrint jasperPrint = null;
    String periode=txttahun.getText()+"."+(cmbbulan.getSelectedIndex() + 1);
    parameter.put("Pperiode", periode);
    parameter.put("Pbulan", cmbbulan.getSelectedItem());
    parameter.put("Ptahun", txttahun.getText());
    try {
        Connection c = koneksi.getKoneksiJ();
        URL url = new URL(global.REPORT+ "/PersediaanbarangDagang.jasper");
        InputStream in = url.openStream();
        jasperPrint = JasperFillManager.fillReport(in, parameter, c);
        JasperViewer.viewReport(jasperPrint, false);
        c.close();
        this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,ex.toString());
        this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
        //Logger.getLogger(formlaporan.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

