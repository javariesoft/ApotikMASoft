    void reloadData() {
        try {
            JDBCAdapter j = new JDBCAdapter(c);
            //String period = txtTahun.getText() + "." + (cboBulan.getSelectedIndex() + 1);
            String sql = "SELECT RETUR.KODERETUR,RETUR.TANGGAL,"
                    + "PELANGGAN.NAMA,PENJUALAN.FAKTUR,"
                    + "case RETUR.STATUS "
                    + "when 0 then 'Draft' "
                    + "when 1 then 'Release' "
                    + "when 2 then 'Batal' end as STATUS "
                    + "FROM PENJUALAN INNER JOIN "
                    + "RETUR ON PENJUALAN.ID = RETUR.IDPENJUALAN "
                    + "INNER JOIN PELANGGAN ON RETUR.KODEPELANGGAN = PELANGGAN.KODEPELANGGAN "
                    + "where month(RETUR.TANGGAL)='"+ (cboBulan.getSelectedIndex()+1) +"' and year(RETUR.TANGGAL)='"+txtTahun.getText()+"' AND PELANGGAN.STATUSCABANG='0'";
            if (txtNofaktur.getText().equals("")) {
                sql += "";
            } else {
                sql += " and PENJUALAN.FAKTUR LIKE '%" + txtNofaktur.getText() + "%'";
            }

            j.executeQuery(sql);
            jScrollPane2.getViewport().remove(jTable2);
            jTable2 = new JTable(j);
            //JTableHeader header = jTable2.getTableHeader();
            //header.setBackground(Color.yellow);
            jTable2.setAutoResizeMode(JTable.AUTO_RESIZE_NEXT_COLUMN);
            jTable2.addMouseListener(new java.awt.event.MouseAdapter() {

                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    jTable2MouseClicked(evt);
                }
            });
            jTable2.setFont(new Font("Tahoma", Font.BOLD, 11));
            jScrollPane2.getViewport().add(jTable2);
            jScrollPane2.repaint();
        } catch (Exception ex) {
            Logger.getLogger(FormLogHistory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
 void cetakLaporan(){
        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        HashMap parameter=new HashMap();
        JasperPrint jasperPrint=null;
        parameter.put("koderetur", jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString());
        try { 
            //jasperPrint = JasperFillManager.fillReport("report\\retur.jasper", parameter, koneksi.getKoneksiJ());
            URL url = new URL(global.REPORT + "/retur.jasper");
            InputStream in = url.openStream();
            jasperPrint = JasperFillManager.fillReport(in, parameter, c);
            JasperViewer.viewReport(jasperPrint,false);
            this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
        } catch (Exception ex) {
            System.out.print(ex.toString());
            //Logger.getLogger(formlaporan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
 
