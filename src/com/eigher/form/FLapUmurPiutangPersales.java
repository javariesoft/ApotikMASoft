    private void btnPreviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviewActionPerformed
        // TODO add your handling code here:
        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        HashMap parameter = new HashMap();
        JasperPrint jasperPrint = null;
        parameter.put("Ptgl1", dateChooserCombo1.getText());
        parameter.put("Ptgl2", dateChooserCombo2.getText());
        parameter.put("Psales", txtkodeSales.getText());
        parameter.put("Pstatus", cboStatBayar.getSelectedIndex());
        try {
            if (pilihan.isSelected()) {
                //jasperPrint = JasperFillManager.fillReport("report\\UmurPiutangPerSalesSemua.jasper", parameter, c);
                URL url = new URL(global.REPORT + "/UmurPiutangPerSalesSemua.jasper");
                InputStream in = url.openStream();
                jasperPrint = JasperFillManager.fillReport(in, parameter, c);
            } else {
                //jasperPrint = JasperFillManager.fillReport("report\\UmurPiutangPerSales.jasper", parameter, c);
                URL url = new URL(global.REPORT + "/UmurPiutangPerSales.jasper");
                InputStream in = url.openStream();
                jasperPrint = JasperFillManager.fillReport(in, parameter, c);
            }
            JasperViewer.viewReport(jasperPrint, false);
            this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
        } catch (Exception ex) {
            System.out.print(ex.toString());
            //Logger.getLogger(formlaporan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnPreviewActionPerformed

